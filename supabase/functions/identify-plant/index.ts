import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { query } = await req.json();
    const GEMINI_API_KEY = Deno.env.get('GEMINI_API_KEY');
    
    if (!GEMINI_API_KEY) {
      throw new Error('GEMINI_API_KEY is not configured');
    }

    console.log('Identifying plant:', query);

    const prompt = `You are a botanical expert. Analyze the following plant description and provide comprehensive information:

Plant Query: "${query}"

Please provide the following information in a structured format:
1. Scientific Name (with pronunciation)
2. Common Names (multiple if available)
3. Plant Family and Classification
4. Physical Description (leaves, flowers, stems, size)
5. Native Habitat and Distribution
6. Care Requirements (sunlight, water, soil, temperature)
7. Interesting Facts or Uses (medicinal, culinary, ornamental)
8. Potential Issues (toxicity, pests, diseases)
9. Similar Species (that might be confused with this plant)

Please be detailed and informative. If the query doesn't match a specific plant, suggest the closest matches.`;

    const response = await fetch(
      `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.0-flash-exp:generateContent?key=${GEMINI_API_KEY}`,
      {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          contents: [{
            parts: [{
              text: prompt
            }]
          }],
          generationConfig: {
            temperature: 0.7,
            topK: 40,
            topP: 0.95,
            maxOutputTokens: 2048,
          }
        }),
      }
    );

    if (!response.ok) {
      const errorText = await response.text();
      console.error('Gemini API error:', response.status, errorText);
      throw new Error(`Gemini API error: ${response.status}`);
    }

    const data = await response.json();
    console.log('Gemini response received');
    
    const plantInfo = data.candidates?.[0]?.content?.parts?.[0]?.text || 'No information found';

    return new Response(
      JSON.stringify({ 
        plantInfo,
        success: true 
      }),
      { 
        headers: { 
          ...corsHeaders, 
          'Content-Type': 'application/json' 
        } 
      }
    );
  } catch (error) {
    console.error('Error in identify-plant function:', error);
    return new Response(
      JSON.stringify({ 
        error: error instanceof Error ? error.message : 'Unknown error',
        success: false 
      }),
      { 
        status: 500,
        headers: { 
          ...corsHeaders, 
          'Content-Type': 'application/json' 
        } 
      }
    );
  }
});
